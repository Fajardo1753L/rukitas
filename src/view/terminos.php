    <p class="parrafo__terminos">Al realizar una compra en nuestra tienda online o solicitar un domicilio, usted acepta los siguientes términos y condiciones: <br><br>

    Los precios de los productos incluyen el IVA, pero no los gastos de envío, 
    que se calcularán según la distacia destino de entrega del pedido, los gastos de envio van de parte del cliente salvo a que se aplique algun descuento o promocion.
    
    Los productos se enviarán a la dirección que usted indique en el formulario de compra, 
    una vez confirmado el pago. <br>
    
    El plazo de entrega estimado es de 3 a 5 días hábiles, 
    salvo que se indique lo contrario en la descripción del producto o se produzcan circunstancias ajenas a nuestro control.
    
    Usted puede pagar su pedido mediante transferencia bancaria o efectivo. 
    
    Usted tiene derecho a desistir de su compra en un plazo de 14 días naturales desde la recepción del pedido, 
    siempre y cuando los productos estén en perfecto estado, sin usar y con su embalaje original. 
    Para ejercer este derecho, debe comunicarnos su decisión por escrito a través de nuestro correo electrónico o formulario de contacto, 
    indicando el número de pedido y los productos que desea devolver. Nosotros le indicaremos cómo proceder para enviar los productos a nuestro almacén. 
    Los gastos de devolución correrán por su cuenta, salvo que el motivo de la devolución sea un defecto o un error por nuestra parte.
    Una vez recibamos los productos devueltos y comprobemos su estado, 
    le reembolsaremos el importe correspondiente mediante el mismo método de pago que utilizó para realizar la compra,
    en un plazo máximo de 14 días naturales desde la fecha en que nos comunique su decisión de desistir.
    Si recibe un producto defectuoso, dañado o equivocado, debe notificárnoslo lo antes posible a través de nuestro correo electrónico o formulario de contacto, 
    adjuntando una fotografía del producto y el número de pedido. Nosotros le ofreceremos una solución satisfactoria, 
    que puede consistir en el cambio del producto por otro o un descuento para futuras compras.
    Nuestra responsabilidad por los productos vendidos se limita al precio de compra de los mismos. 
    No nos hacemos responsables por daños indirectos, consecuenciales o especiales que puedan derivarse del uso o la imposibilidad de uso de los productos.
    Nos reservamos el derecho de modificar los precios, las ofertas, los productos y los términos y condiciones en cualquier momento y sin previo aviso.
    Estos términos y condiciones se rigen por la ley colombiana. Cualquier controversia que pueda surgir entre usted y nosotros se someterá a la jurisdicción de los tribunales competentes.
